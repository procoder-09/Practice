import numpy as np

# 1D array
arr1 = np.array([1, 2, 3, 4])
print(arr1)  # [1 2 3 4]

# 2D array
arr2 = np.array([[1, 2], [3, 4]])
print(arr2)

print("-----------------------------------------------")

a = np.array([1, 2, 3])
b = np.array([4, 5, 6])

print(a + b)  # [5 7 9]
print(a * b)  # [4 10 18]
print(a ** 2) # [1 4 9]

print("-----------------------------------------------")
arr = np.array([1, 2, 3, 4, 5])

print(np.mean(arr))  # 3.0
print(np.sum(arr))   # 15
print(np.max(arr))   # 5
print(np.min(arr))   # 1
